// NESTED FOR LOOP

// *            i=0 (1)
// * *          i=1 (2)
// * * *
// * * * *
// * * * * *

// rows = 5 (Outer Loop)


for (var i = 0; i < 5; i++) {
  for (var j = 0; j <= i; j++) {
    document.write("* ");
  }
  document.write("<br>");
}


